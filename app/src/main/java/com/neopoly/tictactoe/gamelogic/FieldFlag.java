package com.neopoly.tictactoe.gamelogic;

/**
 * This enum type bundles the three possible field flags of a two person game which are an empty
 * field flag, a flag for fields owned by the first and a flag for fields owned by the second gamer.
 */
public enum FieldFlag {
    EMPTY_FIELD,
    FIRST_GAMERS_FLAG,
    SECOND_GAMERS_FLAG;
}
